from pylab import *

figure(figsize=(12, 8), dpi=100)
x = linspace(-2 * pi, 2 * pi, 100)
subplot(221)
plot(x, sin(x))
subplot(422)
plot(x, sin(x) / x)
subplot(424)
plot(x, exp(-x ** 2))
subplot(413)
plot(x, sqrt(1 - x ** 2))
subplot(4, 3, 10)
plot(x, x ** 2)
subplot(4, 3, 11)
plot(x, 1 / x)
subplot(4, 3, 12)
plot(x, cos(x * 5))
show()
